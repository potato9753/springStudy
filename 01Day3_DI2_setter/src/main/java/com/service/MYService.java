package com.service;

public class MYService {

	//변수, 프라퍼티(property)
	public String name;
	public int age;
	
	public MYService() {
	}
	
	public MYService(String name, int age) {
		this.name = name;
		this.age = age;
	}

	public void setName(String name) {
		this.name = name;
	}

	public void setAge(int age) {
		this.age = age;
	}
	
	
}
